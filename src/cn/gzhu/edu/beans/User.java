package cn.gzhu.edu.beans;

public class User {
    String upet_name;
    String uphone;
    String upsw;
    int uage;
    String usex;
    String uaddress;
    String ujoin_time;

    public User() {
    }

    public String getUpet_name() {
        return upet_name;
    }

    public void setUpet_name(String upet_name) {
        this.upet_name = upet_name;
    }

    public String getUphone() {
        return uphone;
    }

    public void setUphone(String uphone) {
        this.uphone = uphone;
    }

    public String getUpsw() {
        return upsw;
    }

    public void setUpsw(String upsw) {
        this.upsw = upsw;
    }

    public int getUage() {
        return uage;
    }

    public void setUage(int uage) {
        this.uage = uage;
    }

    public String getUsex() {
        return usex;
    }

    public void setUsex(String usex) {
        this.usex = usex;
    }

    public String getUaddress() {
        return uaddress;
    }

    public void setUaddress(String uaddress) {
        this.uaddress = uaddress;
    }

    public String getUjoin_time() {
        return ujoin_time;
    }

    public void setUjoin_time(String ujoin_time) {
        this.ujoin_time = ujoin_time;
    }
}
